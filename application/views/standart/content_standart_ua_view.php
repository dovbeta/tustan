<p><u><font size="5"><b>Стандарт</b></font></u></p>
<p><i style="text-align: justify;"><span style="font-size: 14pt; font-family: 'Times New Roman', serif; background-color: rgb(240, 247, 160);" lang="UK">Готель "ТуСтань"</span></i><span style="text-align: justify; font-size: 14pt; font-family: 'Times New Roman', serif; background-color: rgb(240, 247, 160);" lang="UK">&nbsp;пропонує високий рівень сучасного комфорту на <strong>відпочинку в Східниці</strong>. Готель побудований для тих, хто звик до якнайкращого, хто спроможний поцінувати справжній смак і стиль.</span></p><p align="LEFT" lang="zxx">Стандарт - це двомісний&nbsp;однокімнатний&nbsp;номер площею 20 м.кв.</p><blockquote style="margin: 0px 0px 0px 40px; border: none; padding: 0px;"><p align="LEFT" lang="zxx">У номері:</p></blockquote><p align="LEFT" lang="zxx"></p><ul><li>двоспальне ліжко або два півтора спальні ліжка,</li><li>паркетне покриття,</li><li>м'яка частина,</li><li>настольна лампа, торшер,</li><li>супутникове ТБ, плазмовий телевізор,</li><li>Wi-Fi,</li><li>холодильник,</li><li>сейф,</li><li>кондиціонер,</li><li>телефони ( 1 в номері, 1 в санвузлі),</li><li>шафа-купе,</li><li>санвузол площею 6 м.кв: душова кабіна, умивальник, дзеркало, біде, унітаз, сушка для волосся, підлога з підігрівом, сушка для рушників, косметичний набір, одноразові тапочки, халати,</li><li>3 рушники на людину,</li><li>фен, ваги.</li></ul><p class="MsoNormal" style="text-align:center" align="center"><b><u><span lang="UK">Ціни на проживання </span><span style="background-color: rgb(240, 247, 160); background-position: initial initial; background-repeat: initial initial;">у готельному комплексі </span></u></b><b><u><span style="background-color: rgb(240, 247, 160); background-position: initial initial; background-repeat: initial initial;" lang="UK">«ТуСтань» </span><span style="background-color: rgb(240, 247, 160); background-position: initial initial; background-repeat: initial initial;">з лікувально-оздоровчою базою</span></u></b><b><u> <span lang="UK"><o:p></o:p></span></u><